/**
 * Enumeração que define tipos de contatos para colisões no jogo.
 */
public enum ContactTypes {
    CHARACTER, // Representa o personagem
    OBSTACLE, // Representa um obstáculo
    GROUND, // Representa o chão
    WALLLEFT, // Parede esquerda
    WALLRIGHT // Parede direita
}
